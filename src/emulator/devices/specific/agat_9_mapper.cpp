Agat9Mapper::Agat9Mapper(InterfaceManager *im, EmulatorConfigDevice *cd):
      AddressableDevice(im, cd)
{
    Agat9Mapper::reset(true);
}

void Agat9Mapper::reset(bool cold)
{
    if (cold) {
        _WR = _RD = _HD = _D8_4 = 0;
        _bl_RAM = _bl_ROM = 1;
    }
}

dsk_tools::Result Agat9Mapper::load_config(SystemData *sd)
{
    dsk_tools::Result res = AddressableDevice::load_config(sd);
    if (!res) return res;

    m_pm = dynamic_cast<Port*>(im->dm->get_device_by_name(cd->get_parameter("pm").value));
    m_ram = dynamic_cast<RAM*>(im->dm->get_device_by_name(cd->get_parameter("ram").value));
    m_bios = dynamic_cast<ROM*>(im->dm->get_device_by_name(cd->get_parameter("bios").value));
    m_page_map = dynamic_cast<RAM*>(im->dm->get_device_by_name(cd->get_parameter("page_map").value));

    m_bios_mask = (m_bios->get_size()==2048)?0x7FF:0xFFF;

    return dsk_tools::Result::ok();
}

unsigned Agat9Mapper::do_access(unsigned rd_wr, unsigned address, unsigned data)
{
    unsigned v = _FFFF;

    const unsigned A12 = (address >> 12) & 1;
    const unsigned A13 = (address >> 13) & 1;
    const unsigned A14 = (address >> 14) & 1;
    const unsigned A15 = (address >> 15) & 1;
    const unsigned A14_15_ = 1 - (A14 & A15);
    const unsigned PM = m_pm->get_direct(0) & 1;

    const unsigned D14_a = (A12     << 0) +
                           (A13     << 1) +
                           (A14_15_ << 2) +
                           (_WR     << 3) +
                           (_RD     << 4) +
                           (_HD     << 5) +
                           (rd_wr   << 6) +
                           (_bl_ROM << 7) +
                           (_bl_RAM << 8) +
                           (PM      << 9);
    const unsigned _D14_v = D14[D14_a];

    const unsigned __vkROM = (_D14_v >> 7) & 1;
    if (__vkROM == 0 && rd_wr == 1) {
        unsigned __F =  (_D14_v >> 3) & 1;
        unsigned __DE = (_D14_v >> 6) & 1;
        if (__F == 0) {
            // ROM F000–FFFF
            v = m_bios->get_value(address & m_bios_mask);
        }
        if (__DE == 0) {
            // ROM D000–EFFF
        }
    }

    const unsigned __vkRAM = (_D14_v >> 2) & 1;
    if (__vkRAM == 0) {
        const unsigned __A12f = (_D14_v >> 0) & 1;
        const unsigned __W    = (_D14_v >> 1) & 1;
        const unsigned __RP   = (_D14_v >> 5) & 1;

        const unsigned segment = (address >> 13) + (__RP << 3);
        const unsigned bank = m_page_map->get_buffer()[segment];
        const unsigned offset = (address & 0x0FFF) | (__A12f << 12);
        const unsigned ram_address = (bank << 13) + offset;

        if (rd_wr == 1) {
            // Read
            v = m_ram->get_value(ram_address);
        } else {
            // Write
            if (__W == 0) {
                m_ram->set_value(ram_address, data);
            }
        }
    }

    return v;
}

void Agat9Mapper::C08X(const unsigned rd_wr, const unsigned address)
{
    unsigned A0 = (address >> 0) & 1;
    unsigned A1 = (address >> 1) & 1;
    unsigned A3 = (address >> 3) & 1;
    unsigned PM = m_pm->get_direct(0) & 1;

    unsigned D3_a = (_D8_4   << 0) +
                    (A0      << 1) +
                    (A1      << 2) +
                    (A3      << 3) +
                    (_WR     << 4) +
                    (rd_wr   << 5) +
                    (_RD     << 6) +
                    (PM      << 7);
    uint8_t D3_v = D03[D3_a];

    _HD =   (D3_v >> 0) & 1;
    _RD =   (D3_v >> 1) & 1;
    _WR =   (D3_v >> 2) & 1;
    _D8_4 = (D3_v >> 3) & 1;
}

unsigned Agat9Mapper::get_value(unsigned address)
{
    if (address < 0xC000 || address >= 0xD000) {
        // RAM or ROM
        return do_access(1, address, _FFFF);
    }
    if (address >= 0xC080 && address <= 0xC09F) {
        // Control register
        C08X(1, address & 0xF);
        return 0xF4 | (_HD << 3)  | (_RD << 1) | (1 - (_WR & 1)); // HD, ЧТ, ~ЗП
    }
    return _FFFF;
}

void Agat9Mapper::set_value(unsigned int address, unsigned int value, bool force)
{
    if (address < 0xC000 || address >= 0xD000) {
        // RAM or ROM
        do_access(0, address, value);
    } else
    if (address >= 0xC080 && address <= 0xC09F) {
        C08X(0, address & 0xF);
    }
}

ComputerDevice * create_agat_9_mapper(InterfaceManager *im, EmulatorConfigDevice *cd)
{
    return new Agat9Mapper(im, cd);
}
